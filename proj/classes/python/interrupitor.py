class Lampada:
    def __init__(self):
        self.ligada = False
        self.desligada = True

    def clicar_interruptor(self):
        self.ligada =  self.desligada

    def status(self):
        if self.ligada:
            return "A lâmpada está cagada"
        return "A lâmpada está descagada"
    






    














































    